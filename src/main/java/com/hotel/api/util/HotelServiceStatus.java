/**
 * @author kkanaparthi
 * 
 * Defines the constants used for Hotel Service Statuses.
 * The Statuses are SUCCESS, and ERROR.
 * SUCCESS denotes a successful execution, and successful response 
 * from the server.
 * ERROR denotes an issue during execution, and results in an error
 * response from the server.
 *
 */
public enum HotelServiceStatus {
    SUCCESS, ERROR; 

}
